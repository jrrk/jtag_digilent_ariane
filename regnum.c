const char *regnum(int num)
{
  for (int i = 0; i < sizeof(regnams)/sizeof(*regnams); i++)
    {
      if (num == regnams[i].num)
        return regnams[i].nam;
    }
  return "unknown";
}

/* This is for debugging those pesky register mapping numbers */

void all_regnums(void)
{
  for (int i = 0; i < sizeof(regnams)/sizeof(*regnams); i++)
    {
      printf("regnum(%.04d) = %s\n", regnams[i].num, regnams[i].nam);
    }
}
